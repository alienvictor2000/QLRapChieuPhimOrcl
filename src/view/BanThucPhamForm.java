    private void ThemVaoGioHang() {
        // lấy mô hình của jtable tblMenu
        DefaultTableModel model = (DefaultTableModel) tblGioHang.getModel();
        // xóa hết hàng trong bảng
        model.setRowCount(0);
        // thêm tất cả những gì trong danh sách vừa lấy vào mô hình bảng
        BanThucPhamController.Them(gioHang, model);
        lblTongTien.setText(StringHelper.ChuyenDoi(gioHang.getTongTien()));
    }

