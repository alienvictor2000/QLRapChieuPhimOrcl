    private void btnThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThanhToanActionPerformed
        if (gioHang.size() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Không có sản phẩm nào được chọn !");
            return;
        }
        BanThucPhamController.Luu(gioHang,this.MaNV);
        JOptionPane.showMessageDialog(rootPane, "Giao dịch thành công! ");
        gioHang.clear();
        ThemVaoGioHang();
        String localDir = System.getProperty("user.dir");
        String s =localDir+"\\src\\jasperreport\\HoaDonTP.jrxml";
        JasperHelper.Report(s, null);

    }//GEN-LAST:event_btnThanhToanActionPerformed

