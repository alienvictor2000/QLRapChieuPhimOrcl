    private void bnCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnCheckActionPerformed
        // TODO add your handling code here:
        if(KiemTra.kiem_tra_so(txtSDT.getText())==false)
        {
            lbLoi.setVisible(true);
        }
        else
        {
            KhachHang kh=MuaVeDB.lay_diem_tich_luy(txtSDT.getText());
            if(kh!=null)
            {
                hoadon.setKhachhang(kh);
                lbLoi.setVisible(false);
                lb2.setText(kh.getDiemTichLuy()+"");
                txtSDT.setEnabled(false);
                HienDiemTichLuy();
              
            }
            else
            {
                lbLoi.setVisible(true);
            }
        }
    }//GEN-LAST:event_bnCheckActionPerformed

