    private void btnMuaVeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMuaVeActionPerformed
        // TODO add your handling code here:
        long bien_kiem_tra=hoadon.Luu_hoa_don_ve();
        System.out.println(bien_kiem_tra);
        if(bien_kiem_tra==-2)
        {
            JOptionPane.showMessageDialog(null, "bạn chưa chọn vé");
        }
        else if(bien_kiem_tra==-3)
        {
            JOptionPane.showMessageDialog(null, "bạn chưa chọn giá vé");
        }
        else if(bien_kiem_tra==-1)
        {
            System.out.println(hoadon.getMaNV());
            JOptionPane.showMessageDialog(null, "Lỗi ! Có thể vé đã được bán");
        }
        else
        {        
            String localDir = System.getProperty("user.dir");
            try {
                System.out.println("Đưa vào report");
                String filename=localDir+"\\src\\Report\\newReport1.jrxml"; 
                HashMap hs=new HashMap();
                hs.put("SoHD",BigDecimal.valueOf(bien_kiem_tra));
                ReportView p=new ReportView(hs,filename);
                p.setBounds(200, 200, 750, 500);
                p.setResizable(false);
                p.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
                p.setVisible(true);
            } catch (JRException ex) {
                Logger.getLogger(MuaVe.class.getName()).log(Level.SEVERE, null, ex);
            }           
            Refresh();   
        }
    }//GEN-LAST:event_btnMuaVeActionPerformed

