    public int BatLoi()
    {
        AnLoi();
        int loi=0;
        if(cbPhim.getSelectedIndex()==-1)
        {
            lb1.setVisible(true);
            loi++;
        }
        if(cbPhong.getSelectedIndex()==-1)
        {
            lb2.setVisible(true);
            loi++;
        }
       return loi;     
    }

