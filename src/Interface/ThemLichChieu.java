    public void khoitao()
    {
        
        cbGio.removeAll();
        cbPhut.removeAll();
        cbPhim.removeAll();
        cbPhong.removeAll();
        
           for(int i=8;i<24;i++)
           {
               gio.add(i);
              cbGio.addItem(""+i);
           }
           for(int i=0;i<60;i=i+5)
           {
               phut.add(i);
               cbPhut.addItem(""+i);
           }
        cbNgay.setCalendar(Calendar.getInstance());
        
        
        phim=ThemLichChieuDB.lay_danh_sach_phim();
        phong=ThemLichChieuDB.lay_danh_sach_phong();
        
        int size=phim.size();
        
        for(int i=0;i<size;i++)
        {
            cbPhim.addItem(phim.get(i).getTenPhim());
        }
        
        size=phong.size();
        for(int i=0;i<size;i++)
        {
            cbPhong.addItem(phong.get(i).getTenPhong());
        }
        cbPhim.setSelectedIndex(-1);
        cbPhong.setSelectedIndex(-1);     
    }
    
